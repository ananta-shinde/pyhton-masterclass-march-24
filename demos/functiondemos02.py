
def add(n1,n2):
    return n1+n2;

def getpercentage(m1,m2,m3):
    return ((m1+m2+m3)/300) * 100

def areaofsquare(width):
    return width*width

def calculateTicketFair(kms,rate):
    return kms*rate

result = add(20,50)
print(result)

result = getpercentage(50,75,50)
print(result)

result = areaofsquare(150)
print(result)

result = calculateTicketFair(150,12)
print(result)